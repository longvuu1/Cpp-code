#include <bits/stdc++.h>
#define ll long long 
using namespace std;
ll n;
int main(){
    cin >> n; // Nhập giá trị n
    for(ll i =1;i<=n;i++){ // cho vòng for chạy từ 1 đến n-1;
        cout << "#" << endl;
    }
    for(ll i =1;i<=n-1;i++){ // cho vòng for chạy từ 1 đến n-1;
        cout << "#" << endl;
    }
    for(ll i =1;i<=n-2;i++){ // cho vòng for chạy từ 1 đến n-2;
        cout << "#" << endl;
    }
    return 0;
}
